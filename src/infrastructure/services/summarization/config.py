def get_user_reference(category: str) -> str:
    if not category:
        return CATEGORY_USER_REFERENCES["default"]

    category_lower = category.lower().strip()

    # Точное совпадение
    if category_lower in CATEGORY_USER_REFERENCES:
        return CATEGORY_USER_REFERENCES[category_lower]

    # Поиск по вхождению ключевых слов
    for key, reference in CATEGORY_USER_REFERENCES.items():
        if key != "default" and key in category_lower:
            return reference

    # Дефолтное значение
    return CATEGORY_USER_REFERENCES["default"]
